/*************************************************
 * 找出字符串中连续出现最多的字符和个数
 ************************************************/

let s = '232323112130113110dfdfdffd1111';
let s1 = '232aaaaaaabbcdfdkfdjkf323112130113110dfdfdffd1111';

// ::::关键还是正则，会默写吗？
let r = /(.)\1+/g

// ::::s.match 不是 split
let a = s.match(r);

console.log(a)
// [ '11', '11', '11', 'ff', '1111' ]


let b = s1.match(r);
console.log(b);
// [ 'aaaaaaa', 'bb', '11', '11', '11', 'ff', '1111' ]
